uint8_t Font_5x7::getCharWidth(char c) {
  //  i           .
  if (c == 105 || c == 46) {
    return 1;
  }
  //  ,          l
  if (c == 44 || c == 108) {
    return 2;
  }
  //  :          >          I          J          j
  if (c == 58 || c == 62 || c == 73 || c == 74 || c == 106) {
    return 3;
  }
  //  m           w
  if (c == 109 || c == 119) {
    return 5;
  }

  //  a     -    z
  if (c >= 97 && c <= 122) {
    return 4;
  }
  //   0     -    9            A     -    Z            space
  if ((c >= 48 && c <= 57) || (c >= 65 && c <= 90) || (c == 32)) {
    return 5;
  }

  // default
  return 0;
}

uint8_t Font_5x7::getCharHeight(char c) {
  return 7;
}

uint16_t Font_5x7::getCharDataStartOffset(char c) {
  if (c == 32) { // space
    return 0;
  }
  if (c >= 65 && c <= 90) { // A-Z
    return (c - 65) * 5;
  }
  if (c >= 97 && c <= 122) { // a-z
    return 1120 + ((c - 97) * 5);
  }
  if (c >= 48 && c <= 52) { // 0-4
    return 130 + ((c - 48) * 5);
  }
  if (c >= 53 && c <= 57) { // 5-9
    return 1250 + ((c - 53) * 5);
  }
  if (c == 44) { // ,
    return 158;
  }
  if (c == 46) { // .
    return 1278;
  }
  if (c == 58) { // :
    return 155;
  }
  if (c == 62) { // >
    return 1275;
  }

  // default
  return 0;
}

uint16_t Font_5x7::getCharDataRowOffset(char c) {
  return 160;
}

uint8_t Font_5x7::getCharGap(char c) {
  return 1;
}

uint8_t Font_5x7::getCharDrop(char c) {
  //  g           p           q           y
  if (c == 103 || c == 112 || c == 113 || c == 121) {
    return 2;
  }
  return 0;
}

